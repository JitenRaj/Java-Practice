public class _05_String_indexOf {
    public static void main(String[] args) {
        String text = "Hello, i am jiten";
        System.out.println(text.indexOf("Hello"));
        System.out.println(text.indexOf(","));
        System.out.println(text.indexOf("i"));
        System.out.println(text.indexOf("am"));
        System.out.println(text.indexOf("jiten"));
    }
}
