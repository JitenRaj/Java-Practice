public class _08_Switch_default {
    public static void main(String[] args) {
        int day = 5;
        switch (day) {
            case 6:
                System.out.println("Saturday");
                break;
            case 7:
                System.out.println("Sunday");
                break;
            default:
             System.out.println("Looking forward to the weekend");
        }
    }
}