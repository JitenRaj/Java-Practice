public class _05_multiple_parameter {
    static void myMethod(String fname, int age) {
        System.out.println(fname + " is " + age + " years old");
      }
    
    public static void main(String[] args) {
        myMethod("Jitendra",21);
        myMethod("Goutam",17);
        myMethod("Rahul",10);
      }
}
